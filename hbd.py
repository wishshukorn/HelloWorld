import turtle

screen = turtle.getscreen()
screen.bgcolor('#FFF9CA')

tao = turtle.Pen()
tao.width(5)
tao.color('#FFB4B4')
tao.speed(1)

def go(x,y):
    tao.penup()
    tao.goto(x,y)
    tao.pendown()
    
go(-200,200)
tao.right(90)
tao.forward(100)
go(-200,150)
